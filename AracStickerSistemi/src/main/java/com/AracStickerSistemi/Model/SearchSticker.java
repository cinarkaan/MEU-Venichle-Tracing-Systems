package com.AracStickerSistemi.Model;

public class SearchSticker {

    private String stickerNo;
    private String StickerTur;
    private String Kampus;
    private String KapmusBirimi;

    public String getStickerNo() {
        return stickerNo;
    }

    public void setStickerNo(String stickerNo) {
        this.stickerNo = stickerNo;
    }

    public String getStickerTur() {
        return StickerTur;
    }

    public void setStickerTur(String stickerTur) {
        StickerTur = stickerTur;
    }

    public String getKampus() {
        return Kampus;
    }

    public void setKampus(String kampus) {
        Kampus = kampus;
    }

    public String getKapmusBirimi() {
        return KapmusBirimi;
    }

    public void setKapmusBirimi(String kapmusBirimi) {
        KapmusBirimi = kapmusBirimi;
    }
}
